USE DATASETS;

DROP TABLE IF EXISTS T_ROUTES;

CREATE TABLE T_ROUTES
(
	ROUTE_ID	INTEGER NOT NULL PRIMARY KEY,
	ROUTE_NAME	VARCHAR(25),
	OBJECT_ID	INTEGER NOT NULL
);
ALTER TABLE T_ROUTES ADD FOREIGN KEY FK_T_RUT_OBJS (OBJECT_ID) REFERENCES T_OBJECTS(OBJECT_ID) ON UPDATE CASCADE ON DELETE CASCADE;
